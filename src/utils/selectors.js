// pipe = barre verticale
// Créer plusieurs string (tableau) à chaque |
export const pipe = (data) => {
  const pipes = data.split('|');
  return pipes;
};

// Créer plusieurs string (tableau) à chaque -
export const hyphen = (data) => {
  const hyphenn = data.split('-');
  return hyphenn;
};

